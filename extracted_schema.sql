--
-- Name: generate_next_client_id(); Type: FUNCTION; Schema: public; Owner: postgres
--

CREATE FUNCTION public.generate_next_client_id() RETURNS text
    LANGUAGE plpgsql
    AS $_$
DECLARE
    next_num INTEGER;
    new_id TEXT;
BEGIN
    -- Get the highest existing client number
    -- Cast id to TEXT first to handle both UUID and TEXT types
    SELECT COALESCE(MAX(
        CASE 
            WHEN id::TEXT ~ '^CLIENT-[0-9]+$' THEN 
                CAST(SUBSTRING(id::TEXT FROM 8) AS INTEGER)
            ELSE 
                0
        END
    ), 0) + 1 INTO next_num
    FROM clients;
    
    -- Format as CLIENT-XXX with zero-padding (3 digits)
    new_id := 'CLIENT-' || LPAD(next_num::TEXT, 3, '0');
    
    RETURN new_id;
END;
$_$;


